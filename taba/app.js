/* גוש וחלקה — split-screen viewer for Israel's cadastre and planning layers.
 *
 * Map half: OSM base + govmap WMS overlays of the selected layers (image tiles,
 * so no CORS issue). Clicking the map queries that point; the floating button
 * queries the device's current location.
 * Info half: scrollable cards with everything govmap returns for the point,
 * per selected layer, via the entitiesByPoint API — CORS-enabled, but requires
 * x-trace-id/x-user-id/x-fingerprint-id headers to be present (any value passes).
 * Offline fallback (Gush/Helka only): local 2km grid tiles in data/tiles/,
 * generated by download_data.py from govmap's WFS (which has no CORS headers).
 * All server coordinates and geometries are EPSG:3857 meters.
 */
