let wmsLayer = null;

/** (Re)build the WMS overlay from the currently checked layers. */
function updateWms() {
  const active = LAYERS.filter(l => l.on).map(l => l.wms).join(',');
  if (wmsLayer) { wmsLayer.remove(); wmsLayer = null; }
  if (!active) return;
  wmsLayer = L.tileLayer.wms(GEOSERVER, {
    layers: active,
    styles: active,
    format: 'image/png',
    transparent: true,
    version: '1.3.0',
    maxZoom: 19,
    opacity: 0.8,
  }).addTo(map);
}
updateWms();

