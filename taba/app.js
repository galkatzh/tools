function onPosition(lat, lng, accuracy) {
  lastPos = { lat, lng };
  posMarker.setLatLng(lastPos);
  accCircle.setLatLng(lastPos).setRadius(accuracy);
  if (!posMarker._map) { posMarker.addTo(map); accCircle.addTo(map); }
  if (!centered) { centered = true; map.setView(lastPos, 17); }
}

if (mockPos) {
  setTimeout(() => onPosition(mockPos.lat, mockPos.lng, 12), 100);
} else if (!navigator.geolocation) {
  toast('הדפדפן לא תומך באיתור מיקום');
} else {
  navigator.geolocation.watchPosition(
    pos => onPosition(pos.coords.latitude, pos.coords.longitude, pos.coords.accuracy),
    err => toast('שגיאת מיקום: ' + err.message),
    { enableHighAccuracy: true, maximumAge: 5000 });
}

