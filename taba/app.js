// --- Geometry helpers ---

/** Even-odd point-in-polygon over all rings (outer + holes) of a GeoJSON (Multi)Polygon. */
function containsPoint(geom, x, y) {
  const polys = geom.type === 'MultiPolygon' ? geom.coordinates : [geom.coordinates];
  return polys.some(rings => {
    let inside = false;
    for (const ring of rings) {
      for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
        const [xi, yi] = ring[i], [xj, yj] = ring[j];
        if ((yi > y) !== (yj > y) && x < (xj - xi) * (y - yi) / (yj - yi) + xi) inside = !inside;
      }
    }
    return inside;
  });
}

function parseRing(s) {
  return s.split(',').map(pt => pt.trim().split(/\s+/).map(Number));
}

/** WKT POLYGON/MULTIPOLYGON (as returned by entitiesByPoint) -> GeoJSON-style geometry. */
function wktToMultiPolygon(wkt) {
  if (wkt.startsWith('MULTIPOLYGON')) {
    const inner = wkt.slice(wkt.indexOf('(((') + 3, wkt.lastIndexOf(')))'));
    return { type: 'MultiPolygon', coordinates: inner.split(')),((').map(p => p.split('),(').map(parseRing)) };
  }
  if (wkt.startsWith('POLYGON')) {
    const inner = wkt.slice(wkt.indexOf('((') + 2, wkt.lastIndexOf('))'));
    return { type: 'MultiPolygon', coordinates: [inner.split('),(').map(parseRing)] };
  }
  throw new Error('unsupported WKT: ' + wkt.slice(0, 40));
}

let highlight = null;
let highlightBtn = null;

/** Toggle the orange boundary of a (Multi)Polygon geometry on the map. */
function toggleHighlight(geom, btn) {
  const same = btn === highlightBtn;
  if (highlight) { highlight.remove(); highlight = null; }
  if (highlightBtn) { highlightBtn.classList.remove('active'); highlightBtn = null; }
  if (same) return;
  const latlngs = geom.coordinates.map(rings => rings.map(ring => ring.map(([x, y]) => fromMercator(x, y))));
  highlight = L.polygon(latlngs, { color: '#e65100', weight: 3, fillOpacity: 0.08 }).addTo(map);
  map.fitBounds(highlight.getBounds());
  highlightBtn = btn;
  btn.classList.add('active');
}

// --- Point queries ---
const results = document.getElementById('results');
const queryMarker = L.marker([0, 0]);
const tileCache = new Map(); // "ix_iy" -> parsed tile JSON (or null for missing)
const hex32 = () => [...crypto.getRandomValues(new Uint8Array(16))].map(b => b.toString(16).padStart(2, '0')).join('');

/** Live query of all checked layers via govmap's entitiesByPoint. */
async function queryLive(x, y) {
  const layers = LAYERS.filter(l => l.on).map(l => ({ layerId: l.id }));
  // ~3px tolerance at the current zoom, like govmap itself
  const tolerance = 3 * 156543.03392 / 2 ** map.getZoom();
  const res = await fetch(ENTITIES_API, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-trace-id': hex32(), 'x-user-id': hex32(), 'x-fingerprint-id': hex32(),
    },
    body: JSON.stringify({ point: [x, y], layers, tolerance }),
  });
  if (!res.ok) throw new Error(`entitiesByPoint HTTP ${res.status}`);
  const { data, error } = await res.json();
  if (error) throw new Error(`entitiesByPoint: ${error}`);
  return data || [];
}

/** Fetch (and cache) the 2km data tile containing mercator point (x, y). */
async function getTile(x, y) {
  const key = `${Math.floor(x / TILE_SIZE)}_${Math.floor(y / TILE_SIZE)}`;
  if (!tileCache.has(key)) {
    const res = await fetch(`data/tiles/${key}.json`);
    if (res.status === 404) {
      tileCache.set(key, null); // area with no cadastre data
    } else if (!res.ok) {
      throw new Error(`tile ${key}: HTTP ${res.status}`);
    } else {
      tileCache.set(key, await res.json());
    }
  }
  return tileCache.get(key);
}

/** Offline fallback: Gush/Helka from the local tiles, shaped like entitiesByPoint output. */
async function queryLocal(x, y) {
  const tile = await getTile(x, y);
  const ent = (rec, fields) => rec && {
    geom: rec.geom,
    fields: Object.entries(fields).map(([fieldName, fieldValue]) => ({ fieldName, fieldValue, isVisible: true })),
  };
  const parcel = tile?.parcels.find(p => containsPoint(p.geom, x, y));
  const gush = tile?.gushim.find(g => containsPoint(g.geom, x, y));
  return [
    { caption: 'גושים', entities: [ent(gush, gush && { 'מספר גוש': gush.gush_num, 'תת גוש': gush.gush_suffix, 'סטטוס': gush.status_text })].filter(Boolean) },
    { caption: 'חלקות', entities: [ent(parcel, parcel && { 'מספר גוש': parcel.gush_num, 'תת גוש': parcel.gush_suffix, 'חלקה': parcel.parcel, 'שטח רשום (מ"ר)': parcel.legal_area, 'סטטוס': parcel.status_text })].filter(Boolean) },
  ];
}

/** Format a field value for display: links, ISO dates, numbers.
 * Thousands separators only on area fields — gush/parcel/plan numbers are identifiers. */
function formatValue(v, fieldName) {
  if (v === null || v === undefined || v === '') return '—';
  if (typeof v === 'string' && /^https?:\/\//.test(v)) {
    const a = document.createElement('a');
    a.href = v; a.target = '_blank'; a.rel = 'noopener'; a.textContent = 'קישור';
    return a;
  }
  if (typeof v === 'string' && /^\d{4}-\d{2}-\d{2}T/.test(v)) {
    return new Date(v).toLocaleDateString('he-IL');
  }
  if (typeof v === 'number' && /שטח/.test(fieldName)) return v.toLocaleString('he-IL');
  return String(v);
}

/** Render entitiesByPoint-shaped results into the info pane. */
function renderResults(layers, note) {
  results.replaceChildren();
  if (note) {
    const div = document.createElement('div');
    div.className = 'offline-note';
    div.textContent = note;
    results.append(div);
  }
  let any = false;
  for (const layer of layers) {
    const ents = layer.entities || [];
    if (!ents.length) continue;
    any = true;
    const card = document.createElement('div');
    card.className = 'layer-card';
    const h = document.createElement('h3');
    h.textContent = layer.caption + (ents.length > 1 ? ` (${ents.length})` : '');
    card.append(h);
    for (const ent of ents) {
      const div = document.createElement('div');
      div.className = 'entity';
      const head = document.createElement('div');
      head.className = 'entity-head';
      const btn = document.createElement('button');
      btn.textContent = 'הצג גבול';
      btn.onclick = () => {
        try {
          // WKT is parsed lazily: plan geometries can be 100KB+ each
          ent.parsed ??= typeof ent.geom === 'string' ? wktToMultiPolygon(ent.geom) : ent.geom;
          toggleHighlight(ent.parsed, btn);
        } catch (e) {
          console.error('highlight failed', e);
          toast('לא ניתן להציג את הגבול');
        }
      };
      head.append(btn);
      div.append(head);
      const table = document.createElement('table');
      for (const f of ent.fields) {
        if (!f.isVisible) continue;
        const tr = table.insertRow();
        tr.insertCell().textContent = f.fieldName;
        tr.insertCell().append(formatValue(f.fieldValue, f.fieldName));
      }
      div.append(table);
      card.append(div);
    }
    results.append(card);
  }
  if (!any) {
    const p = document.createElement('p');
    p.className = 'hint';
    p.textContent = 'לא נמצא מידע בשכבות שנבחרו עבור מיקום זה.';
    results.append(p);
  }
}

/** Query a map location and fill the info pane. */
async function queryPoint(latlng) {
  if (highlight) { highlight.remove(); highlight = null; highlightBtn = null; }
  queryMarker.setLatLng(latlng);
  if (!queryMarker._map) queryMarker.addTo(map);
  results.replaceChildren(Object.assign(document.createElement('p'), { className: 'hint', textContent: 'טוען…' }));
  const { x, y } = toMercator(latlng.lat, latlng.lng);
  try {
    renderResults(await queryLive(x, y));
  } catch (e) {
    console.error('live query failed, falling back to local Gush/Helka tiles', e);
    try {
      renderResults(await queryLocal(x, y), `השליפה החיה מ-govmap נכשלה (${e.message}) — מוצגים נתוני גוש/חלקה מקומיים בלבד`);
    } catch (e2) {
      console.error('local fallback failed too', e2);
      toast('שגיאה בשליפת מידע: ' + e2.message);
      results.replaceChildren(Object.assign(document.createElement('p'), { className: 'hint', textContent: 'שגיאה בשליפת המידע.' }));
    }
  }
}

map.on('click', e => queryPoint(e.latlng));

document.getElementById('here-btn').onclick = () => {
  if (!lastPos) { toast('עדיין אין מיקום — ודאו שאיתור מיקום מאושר'); return; }
  map.setView(lastPos);
  queryPoint(lastPos);
};
